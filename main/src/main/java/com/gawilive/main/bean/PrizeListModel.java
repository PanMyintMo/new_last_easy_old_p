package com.gawilive.main.bean;

public class PrizeListModel {

    private String id;
    private String title;
    private String type_val;
    private String thumb;
    private String rate;
    private String thumb_str;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getType_val() {
        return type_val;
    }

    public void setType_val(String type_val) {
        this.type_val = type_val;
    }

    public String getThumb() {
        return thumb;
    }

    public void setThumb(String thumb) {
        this.thumb = thumb;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getThumb_str() {
        return thumb_str;
    }

    public void setThumb_str(String thumb_str) {
        this.thumb_str = thumb_str;
    }
}
