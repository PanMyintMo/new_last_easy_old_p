package com.gawilive.common.bean;

public class AuthModel {

    private String uid;
    private String real_name;
    private String mobile;
    private String cer_no;
    private String front_view;
    private String back_view;
    private String handset_view;
    private String status;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getReal_name() {
        return real_name;
    }

    public void setReal_name(String real_name) {
        this.real_name = real_name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getCer_no() {
        return cer_no;
    }

    public void setCer_no(String cer_no) {
        this.cer_no = cer_no;
    }

    public String getFront_view() {
        return front_view;
    }

    public void setFront_view(String front_view) {
        this.front_view = front_view;
    }

    public String getBack_view() {
        return back_view;
    }

    public void setBack_view(String back_view) {
        this.back_view = back_view;
    }

    public String getHandset_view() {
        return handset_view;
    }

    public void setHandset_view(String handset_view) {
        this.handset_view = handset_view;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
